
<h1>Was kann Livread</h1>
<ul>
	<li>Ihre sämtlichen elektronischen Bücher komfortabel verwalten</li>
	<li>Bücher in verschiedene Formate konvertieren</li>
	<li>Bücher versionieren</li>
	<li>Beinahe beliebige Textdokumente vorlesen lassen</li>
	<li>Vorlesesystem für sehbehinderte Menschen</li>
	<li>Vorlesesystem für körperlich eingeschränkte oder alte Menschen</li>
	<li>Exzerpieren von Dokumenten</li>
</ul>

<h1>Wie ist Livread aufgebaut</h1>
<p>Livread hat verschiedene Lesemodi
<ul>
	<li>Einen reinen eBook-Modus, um perfektes Lese-Feeling insbesondere auf Tablet-PCs, Smartphones, eBook-Reader zu erreichen</li>
	<li>Einen graphischen integrierten Modus, der sich in die Bibliotheksstruktur nahtlos einfügt</li>
	<li>Einen Exzerpier- und Vorlesemodus, der beliebige Dokumente in Textdateien umwandelt und zur weiteren Bearbeitung vorbereitet</li>
</ul>


<h1>Steuerung des Leseprozesses</h1>
<ul>
	<li>Im klassischen eBook-Modus</li>
	<ul>
		<li>Blättern</li>
		<ul>
			<li>kannst Du mit den Cursor-(Pfeil)tasten durch das Buch blättern</li>
			<li>kannst Du mit per Toch-and-Move mit dem Touchpad, dem berührungsempfindlichen Bildschirm oder der Maus umblättern</li>
			<li>Umblättern durch Klicken auf den rechten, bzw. linken Bereich der dargestellten Buchseite</li>
			<li>Ein Wort zu Seitenzahlen: da das Buch in verschiedenen Schriftgrößen (sowohl innerhalb von Livread, als auch Browser- und Systemeinstellungen) dargestellt werden kann, machen Seitenzahlen keinen Sinn, daher speichert Livread den Fortschritt des Lesevorgangs. Da epub-Dokumente in verschiedene <em>Unterdokumente</em>, vorzugsweise Abschnitte eingeteilt sind, werden im eBook-Modus lediglich das jeweilige Kapitel, sowie die Seitenanzahl des jeweiligen Kapitels dargestellt.</li>
			<li>Keine Angst, Livread speichert die Leseposition.</li>
		</ul>
		<li>Weitere Navigation</li>
		<ul>
			<li>Durch Klicken auf den Buchtitel im linken oberen Bereich des eBook-Lesemodus kannst Du Dir ein Inhaltsverzeichnis darstellen lassen, mit dessen Links du sehr schnell im Buch navigieren kannst</li>
			<li>Beim Klicken auf die Seitenzahl kannst Du einen Scroller-Balken anzeigen lassen, mit dem du elegant durch das gesamte Buch blättern kannst</li>
		</ul>
	</ul>
	<li>Im integrierten Lesemodus, sowie im Exzerpier- und Vorlesemodus</li>
		<ul>
			<li>kannst Du mit den Cursor-(Pfeil)tasten durch das Buch blättern</li>
			<li>Anhand der Navigationslinks im Infobereich links blättern</li>
			<li>Beim eBook-Integriert-Modus kannst Du wie im klassischen Modus umblättern</li>
		</ul>
</ul>

<h1>Die Kontexthilfe</h1>
<p>In jedem Bereich der Bedienoberfläche wird dir links eine Kurzeinweisung bereitgestellt, die schnell auf die möglichen oder erforderlichen Aktionen hinweist.</p>


<h1>Buch öffnen</h1>
<p>Verwende dazu den Menüeintrag Datei - Buch öffnen. Klicke dann auf die Pluszeichen des jeweiligen Autoren, dessen Buch Du lesen möchtest. Du kannst das Buch einfach anklicken, um es im voreingestellten Lesemodus (Siehe Menü) zu lesen.</p>
<p>Wenn Du auf Autoren oder Bücher mit der rechten Maustaste klickst, erscheint ein Kontextmenü mit weiteren Funktionen.</p>

<h1>Markierungen</h1>
<p>Du kannst einzelne Textpassagen markieren und unter dem Menüeintrag Buch dann <em>Markierung als Notiz</em> wählen.</p>
<p>Dies sollte vorzugsweise im Exzerpiermodus stattfinden.</p>
<p>Im Exzerpiermodus kann per Doppelklick auf ein Wort ein Englisch-Lexikon abgefragt werden.</p>


<h1>Datenbank aktualisieren</h1>
<p>
Dieser Modus stellt Büchereien, die Bücher auch per FTP hochladen können die Möglichkeit zur Verfügung, sämtliche Änderungen mit der Datenbank automatisch zu synchronisieren. Alle B&uuml;cher werden im Format <dd>Autorname/Titel des Buches/Titel des Buches - Autorname.(txt|epub)</dd> physikalisch verwaltet. 
</p>
<p>
Werden neue Dateien im Dateisystem in der Form gefunden, so werden sie in die Datenbank mitaufgenommen. Wurden Dateien entfernt, so werden die B&uuml;cher aus der Datenbank (books, contents, bookmarks) ebenfalls  entfernt.
</p>
<p>
Hat sich die Dateigr&ouml;&szlig;e ge&auml;ndert, wird der Inhalt neu eingelesen und bereits vorhandene Bookmarks und Paginations (contents) entfernt.
</p>
<p>
Vorzugsweise sollten epub- oder reine Text-Dateien in UTF8-Format verwendet werden.
</p>
